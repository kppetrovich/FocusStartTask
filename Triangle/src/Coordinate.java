public class Coordinate {
    private int x;
    private int y;

    public Coordinate(int x, int y) {
        this.x = x;
        this.y = y;
    }

    public int getX() {
        return x;
    }

    public void setX(int a) {
        this.x = x;
    }

    public int getY() {
        return y;
    }

    public void setB(int b) {
        this.y = y;
    }
}
